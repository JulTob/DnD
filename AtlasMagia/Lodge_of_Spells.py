class Spell:
	def __init__(self,
				 name="",
				 level=0,
				 school="",
				 casting_time="",
				 ranges = "",
				 duration = "",
				 components = "",
				 concentration = "",
				 definition = ""):
		self.name =  name
		self.level = level
		self.school = school
		self.casting_time = casting_time
		self.ranges = ranges
		self.duration = duration
		self.components = components
		self.concentration = concentration
		self.definition = definition

	def describe(self):
		desc = self.string
		return desc

	@property
	def string(self):

		name  = f"{self.name}"
		desc  = f"{self.school}"
		definition = ""
		if self.casting_time:   desc += f"({self.casting_time})"
		if self.concentration:  desc += f"⟨{self.concentration}"
		if self.duration:       desc += f"⟨{self.duration}⟩"
		if self.ranges:         desc += f"- {self.ranges}-"
		if self.definition:     definition += f"\n\t{self.definition}"
		string = Entry(title=name, definition=definition, description=desc)
		return string









